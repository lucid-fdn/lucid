function browserOperatorMetadata(mode: string, vertical: string): Record<string, unknown> {
  return {
    browser_operator: {
      mode,
      vertical,
      capability: 'browser-operator',
      legacyCapability: 'browser-qa',
    },
  }
}

function designOpsMetadata(mode: string, vertical: string): Record<string, unknown> {
  return {
    design_ops: {
      mode,
      vertical,
      capability: 'design-ops',
      runtime_context: serializeDesignOpsForRuntime(buildDesignOpsRuntimeContext()),
      checklist: DESIGN_OPS_CHECKLIST.map((item) => item.id),
    },
    operating_loop: {
      checklist: DESIGN_OPS_CHECKLIST.map((item) => item.id),
    },
  }
}

function releaseQualityMetadata(
  workflowId: AgentOpsWorkflowId,
  mode: string,
  gateTargets: string[] = ['ship', 'deploy', 'promotion'],
): Record<string, unknown> {
  const checks = getReleaseQualityChecksForWorkflow(workflowId)
  return {
    release_quality: {
      mode,
      capability: 'release-quality-gates',
      runtime_context: buildReleaseQualityRuntimeContext({ workflowId }),
      checks: checks.map((check) => check.id),
      required_checks: checks.filter((check) => check.required).map((check) => check.id),
      gate_targets: gateTargets,
    },
    operating_loop: {
      checklist: checks.map((check) => check.id),
    },
  }
}

function defineWorkflow(definition: AgentOpsWorkflowDefinitionInput): AgentOpsWorkflowDefinition {
  const parsed = agentOpsWorkflowSchema.parse(definition)
  return Object.freeze({
    ...parsed,
    triggerPhrases: Object.freeze([...parsed.triggerPhrases]),
    requiredCapabilities: Object.freeze([...parsed.requiredCapabilities]),
    compatibleRuntimeModes: Object.freeze([...parsed.compatibleRuntimeModes]),
    capabilityFallbacks: Object.freeze(Object.fromEntries(
      Object.entries(parsed.capabilityFallbacks).map(([capability, fallbacks]) => [
        capability,
        Object.freeze([...fallbacks]),
      ]),
    )),
    inputFields: Object.freeze(parsed.inputFields.map((field) => Object.freeze({ ...field }))),
    outputSections: Object.freeze([...parsed.outputSections]),
    evidenceTypes: Object.freeze([...parsed.evidenceTypes]),
    approvalGates: Object.freeze(parsed.approvalGates.map((gate) => Object.freeze({ ...gate }))),
    evalPack: Object.freeze(parsed.evalPack.map((scenario) => Object.freeze({ ...scenario }))),
    steps: Object.freeze(parsed.steps.map((step) => Object.freeze({
      ...step,
      dependsOn: Object.freeze([...step.dependsOn]),
      payload: step.payload ? Object.freeze({ ...step.payload }) : undefined,
    }))),
    metadata: Object.freeze({ ...parsed.metadata }),
  }) as AgentOpsWorkflowDefinition
}

function field(
  key: string,
  label: string,
  type: AgentOpsWorkflowDefinition['inputFields'][number]['type'],
  required = true,
  description?: string,
): AgentOpsWorkflowDefinition['inputFields'][number] {
  return { key, label, type, required, description }
}

function agentRunStep(
  id: string,
  title: string,
  dependsOn: string[] = [],
  routeClass: 'fast' | 'strong' | 'external' = 'strong',
): AgentOpsWorkflowDefinition['steps'][number] {
  return {
    id,
    title,
    kind: 'execute',
    stepType: 'scheduled',
    dependsOn,
    routeClass,
    payload: { agent_ops_step: id },
  }
}

function approvalStep(
  id: string,
  title: string,
  dependsOn: string[] = [],
): AgentOpsWorkflowDefinition['steps'][number] {
  return {
    id,
    title,
    kind: 'approval',
    stepType: 'approval',
    dependsOn,
    routeClass: 'strong',
    payload: { agent_ops_step: id },
  }
}

function summarizeStep(
  id: string,
  title: string,
  dependsOn: string[],
): AgentOpsWorkflowDefinition['steps'][number] {
  return {
    id,
    title,
    kind: 'summarize',
    stepType: 'scheduled',
    dependsOn,
    routeClass: 'strong',
    payload: { agent_ops_step: id, output_sections: standardOutputSections },
  }
}

