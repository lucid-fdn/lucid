const workflowById = new Map<AgentOpsWorkflowId, AgentOpsWorkflowDefinition>(
  AGENT_OPS_WORKFLOWS.map((workflow) => [workflow.id, workflow]),
)

const workflowBySlug = new Map<string, AgentOpsWorkflowDefinition>(
  AGENT_OPS_WORKFLOWS.map((workflow) => [workflow.slug, workflow]),
)

export function listAgentOpsWorkflows(): readonly AgentOpsWorkflowDefinition[] {
  return AGENT_OPS_WORKFLOWS
}

export function getAgentOpsWorkflow(id: AgentOpsWorkflowId): AgentOpsWorkflowDefinition {
  const workflow = workflowById.get(id)
  if (!workflow) {
    throw new Error(`Unknown Agent Ops workflow: ${id}`)
  }
  return workflow
}

export function findAgentOpsWorkflowBySlug(slug: string): AgentOpsWorkflowDefinition | null {
  return workflowBySlug.get(slug) ?? null
}

export function isDagBackedWorkflow(workflow: AgentOpsWorkflowDefinition): boolean {
  return workflow.executionMode === 'dag'
}
